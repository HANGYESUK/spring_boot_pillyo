package com.pillyo.pill.model.social;

public class KakaoVO {
	private int k_number;
	private String k_name;
	private String k_email;
	private String k_bday;
	private String k_gender;
	
	public int getK_number() {
		return k_number;
	}
	public void setK_number(int k_number) {
		this.k_number = k_number;
	}
	public String getK_name() {
		return k_name;
	}
	public void setK_name(String k_name) {
		this.k_name = k_name;
	}
	public String getK_email() {
		return k_email;
	}
	public void setK_email(String k_email) {
		this.k_email = k_email;
	}
	public String getK_bday() {
		return k_bday;
	}
	public void setK_bday(String k_bday) {
		this.k_bday = k_bday;
	}
	public String getK_gender() {
		return k_gender;
	}
	public void setK_gender(String k_gender) {
		this.k_gender = k_gender;
	}
}
