package com.pillyo.pill.model;

public class SugarVO {
	private int sugarNo;
	private String situation;
	private String sugarMedication;
	private String acpc;
	private int sugarLevel;
	private String acpcMemo;
	private String sugarRecordDate;
	private String sugarRecordTime;
	private int famNo;
	
	public int getSugarNo() {
		return sugarNo;
	}
	public void setSugarNo(int sugarNo) {
		this.sugarNo = sugarNo;
	}
	public String getSituation() {
		return situation;
	}
	public void setSituation(String situation) {
		this.situation = situation;
	}
	public String getSugarMedication() {
		return sugarMedication;
	}
	public void setSugarMedication(String sugarMedication) {
		this.sugarMedication = sugarMedication;
	}
	public String getAcpc() {
		return acpc;
	}
	public void setAcpc(String acpc) {
		this.acpc = acpc;
	}
	public int getSugarLevel() {
		return sugarLevel;
	}
	public void setSugarLevel(int sugarLevel) {
		this.sugarLevel = sugarLevel;
	}
	public String getAcpcMemo() {
		return acpcMemo;
	}
	public void setAcpcMemo(String acpcMemo) {
		this.acpcMemo = acpcMemo;
	}
	public String getSugarRecordDate() {
		return sugarRecordDate;
	}
	public void setSugarRecordDate(String sugarRecordDate) {
		this.sugarRecordDate = sugarRecordDate;
	}
	public String getSugarRecordTime() {
		return sugarRecordTime;
	}
	public void setSugarRecordTime(String sugarRecordTime) {
		this.sugarRecordTime = sugarRecordTime;
	}
	public int getFamNo() {
		return famNo;
	}
	public void setFamNo(int famNo) {
		this.famNo = famNo;
	}			
}
