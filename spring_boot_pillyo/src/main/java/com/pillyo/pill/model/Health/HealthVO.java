package com.pillyo.pill.model.Health;

public class HealthVO{
	private int famNo;		// 가족번호
	private int bodyNo;		// 체형 관리번호
	private int feelNo;		// 기분 관리번호
	private int pressureNo;	// 혈압 관리번호
	private int sugarNo;	// 혈당 관리번호
	
	
	public int getFamNo() {
		return famNo;
	}
	public void setFamNo(int famNo) {
		this.famNo = famNo;
	}
	// ---------------------------------------------
	public int getBodyNo() {
		return bodyNo;
	}
	public void setBodyNo(int bodyNo) {
		this.bodyNo = bodyNo;
	}
	// ---------------------------------------------
	public int getFeelNo() {
		return feelNo;
	}
	public void setFeelNo(int feelNo) {
		this.feelNo = feelNo;
	}
	// ---------------------------------------------
	public int getPressureNo() {
		return pressureNo;
	}
	public void setPressureNo(int pressureNo) {
		this.pressureNo = pressureNo;
	}
	// ---------------------------------------------
	public int getSugarNo() {
		return sugarNo;
	}
	public void setSugarNo(int sugarNo) {
		this.sugarNo = sugarNo;
	}
	
	
}